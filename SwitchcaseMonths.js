var num = 1;
switch(num)
{
        case 0 :
                console.log(num+ "Sunday")
                break;
        case 1 :
                console.log(num+ "Monday")   
                break;
        case 2 :
                console.log(num+ "Tuesday")
                break;
        case 3 :
                console.log(num+ "Wednesday")
                break;
        case 4 :
                console.log(num+ "Thursday")
                break;
        case 5 :
                console.log(num+ "Friday")
                break;
        case 6 :
                console.log(num+ "Saturday")
                break;

    default:
            console.log("Entered Worong number")
            break;
}